import os
from pathlib import Path

# Configurable file selection.
# Add or remove extensions to control which text/code files are aggregated.
INCLUDE_EXTENSIONS = {'.py', '.yaml', '.yml', '.json', '.ini', '.cfg', '.toml', '.md', '.txt'}
EXCLUDE_DIRS = {'__pycache__', '.venv', '.git'}
OUTPUT_FILENAME = 'repository_aggregation.txt'
BENCHMARK_RESULTS_FILENAME = 'benchmark_results.md'


def is_excluded_dir(path: Path) -> bool:
    return any(part in EXCLUDE_DIRS for part in path.parts)


def should_include_file(path: Path) -> bool:
    if path.name == OUTPUT_FILENAME:
        return False
    if is_excluded_dir(path):
        return False
    return path.suffix.lower() in INCLUDE_EXTENSIONS


def gather_files(root: Path) -> list[Path]:
    files = []
    for dirpath, dirnames, filenames in os.walk(root):
        current_dir = Path(dirpath)
        # Skip excluded directories entirely.
        dirnames[:] = [d for d in dirnames if d not in EXCLUDE_DIRS]

        for filename in sorted(filenames):
            candidate = current_dir / filename
            if should_include_file(candidate) and candidate.name != BENCHMARK_RESULTS_FILENAME:
                files.append(candidate)
    return sorted(files)


def write_aggregation(root: Path, output_path: Path, files: list[Path]) -> None:
    with output_path.open('w', encoding='utf-8') as dst:
        dst.write('Repository aggregation of code and benchmark results.\n')
        dst.write('Generated by aggregate_code_and_benchmarks.py\n\n')

        for path in files:
            relative_path = path.relative_to(root)
            dst.write(f'===== FILE: {relative_path} =====\n')
            try:
                dst.write(path.read_text(encoding='utf-8'))
            except UnicodeDecodeError:
                dst.write('# [Unable to decode file contents]\n')
            dst.write('\n\n')

        benchmark_path = root / BENCHMARK_RESULTS_FILENAME
        dst.write(f'===== BENCHMARK RESULTS: {BENCHMARK_RESULTS_FILENAME} =====\n')
        if benchmark_path.exists():
            dst.write(benchmark_path.read_text(encoding='utf-8'))
        else:
            dst.write(f'# Warning: {BENCHMARK_RESULTS_FILENAME} not found.\n')


def main() -> None:
    root = Path(__file__).resolve().parent
    output_path = root / OUTPUT_FILENAME
    files = gather_files(root)
    write_aggregation(root, output_path, files)
    print(f'Wrote aggregation to {output_path}')


if __name__ == '__main__':
    main()
